"""This module implements
some calculator functions
"""


def add(a, b):
    """Adds two numbers
    a -- first number
    b -- second number
    returns the sum """
    return a + b


def main():
    """Main method. Adds 1 and 2"""
    result = add(1, 2)
    print(result)


if __name__ == "__main__":
    main()

